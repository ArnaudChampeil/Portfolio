    <div class="container">
        <div class="row">
            <div class="col-lg-12 text-center">
                <h2 class="section-heading text-uppercase">Ondes et santé</h2>
                <h3 class="section-subheading text-muted">
                    Les antennes massive MIMO (multi user Multiple Input Multiple Output) qui vont être utilisées, sont dotées de plusieurs canaux ou connexions qui permettent d’envoyer et recevoir plus de données simultanément.<br><br>
                    L’antenne massive MIMO agit comme un phare directionnel. Le signal est dirigé dans une direction précise au lieu d’être émis dans toutes les directions. L’antenne massive MIMO limite ainsi la déperdition d’énergie en émettant les ondes radio uniquement là où elles sont utiles.<br><br>
                    Elle permet de diriger le signal radio vers les utilisateurs et les dispositifs quand cela est nécessaire plutôt que d’émettre en permanence et partout. Ainsi, les ondes superflues aux alentours des utilisateurs sont réduites considérablement, voire éliminées.
                </h3>
                <img src="img/veille/4g-5g.png">
            </div>
            <div class="col-lg-12 text-center">
                <h4 class="section-heading text-uppercase">Risque pour la santé</h4>
                <h3 class="section-subheading text-muted">
                    Il existe plus de 3 500 publications scientifiques (études, rapports, avis...) qui portent sur l’ensemble des radiofréquences au niveau mondial, dont une majorité sur la téléphonie mobile.<br><br>
                    Les autorités sanitaires internationales majeures (OMS : Organisation Mondiale de la Santé) et le comité scientifique de l’UE, ainsi que des autorités nationales déterminantes (Allemagne, France, Royaume-Uni, Canada, Espagne, Pays Nordiques...) arrivent à la même conclusion.<br><br>
                    Il n’existe aucune preuve scientifique d’un risque avéré pour la santé lorsque l’exposition aux ondes radio est inférieure aux seuils recommandés par l’OMS.<br><br>
                    Ces limites sont fréquemment réévaluées par ces différentes autorités sanitaires.<br><br>
                    Les ondes radio de la 5G (700 Mhz et 3 500 Mhz) pour la France sont utilisées depuis plus de cinquante ans par des services quotidiens comme la télévision.<br><br>
                    Les ondes utilisées par la 5G sont donc les mêmes que les ondes de la télévision, de la radio ou de la téléphonie mobile existante.
                </h3>
                <img src="img/veille/onde-quotidien.png">
                <h3 class="section-subheading text-muted">
                    Les limites d’exposition de l’OMS ont été adoptées dans la majorité des pays et s’appliquent à l’ensemble des services des ondes radio. Le respect de ces limites protège la population.<br><br>
                    Les limites d’exposition pour la 5G sont de 39 Volts par mètre (V/m) à 700 MHz équivalentes à celles de la 4G et de 61 V/m à 3 500 MHz équivalentes à celles du Wi-Fi, de la 3G et de la 4G.<br><br>
